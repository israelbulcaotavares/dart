void main(){

  double b = 5;

  double a = b;
  print('Valor b foi atribuído para Valor a = $a');

  //adição com atribuicao
  a = a + b;
  a += b;

  //subtração com atribuicao
  a = a - b;
  a -= b;

  //multiplicacao com atribuicao
  a = a * b;
  a *= b;

  //divisao com atribuicao
  a = a / b;
  a /= b;
}