void main(){

  //E (AND) LOGICO && - TODAS AS CONDIÇÕES DEVEM SER = VERDADEIRAS
  bool andLogico = true && true;
  print(andLogico);


  //OU (OR) LOGICO || PELO MENOS UMA CONDIÇÃO DEVE SER VERDADEIRA
  bool orLogico = true || false;
  print(orLogico);

  //NAO (NOT) LOGICO (NEGACAO) !
  bool notLogico = !false;
  print(notLogico);



}