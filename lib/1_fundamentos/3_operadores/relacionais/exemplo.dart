void main(){
  int a = 5;
  int b = 10;
  // igual ==
  bool ehigual = a == b;
  print(ehigual);

  // diferente !=
  bool naoEhIgual = a != b;
  print(naoEhIgual);

  //maior que >
  bool ehMaiorQue = a > b;
  print(ehMaiorQue);

  //menor que <
  bool ehMenorQue = a < b;
  print(ehMenorQue);

  //maior ou igual que >=
  bool ehMaiorOuIgualQue = a >= b;
  print(ehMaiorOuIgualQue);

  //menor ou igual que <=
  bool ehMenorOuIgualQue = a <= b;
  print(ehMenorOuIgualQue);

}